/*
Colecciones: sirve para almacenar un conjunto de valores y despues poder trabajar con ellos.
 
 Existen 3 grandes tipos para asignar colecciones:
 
 1. Los Arrays o Arreglos: Son colecciones o almacenamientos Ordenadas (Posiciones)
    Array <String> ()
    [String] ()
 
 2. Los Set: Almacena valores de manera Desordenadas o no Ordenadas, son Unicos por lo que no se puede repetir
    Set <String> ()
 
 3. Los Diccionarios o Dictionary: Almacema una serie de datos del formato clave-valor, es decir que posee una clave y esa clave nos dara acceso a ese valor. Son claves Unicas
    Dictionary <Int:String> ()
    [Int:String]()
 
 */

